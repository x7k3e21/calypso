
%include "multiboot/multiboot2.asm"

%define _MB2_HEADER_LENGTH (_MB2_HEADER_END - _MB2_HEADER_START)

    section .multiboot2
_MB2_HEADER_START:
    dd MULTIBOOT2_HEADER_MAGIC
    dd MULTIBOOT2_ARCH_I386

    dd _MB2_HEADER_LENGTH

    dd 0x100000000 - (MULTIBOOT2_HEADER_MAGIC + MULTIBOOT2_ARCH_I386 + _MB2_HEADER_LENGTH)

    dw MULTIBOOT2_HEADER_TAG_END
    dw 0x0000
    dd 0x00000008
_MB2_HEADER_END: